    <h3>Как поменять карьеру</h3>
    <p>А теперь расскажу о том, что делать, если хочется сменить трек.

        Во-первых, убедиться, что ты закрыл все висящие на тебе задачи. И что коллеги могут тебя заменить (это особенно важно, если хочешь поменять трек внутри компании).

        Во-вторых, понять, чем ты хочешь заниматься, и постепенно это изучить. Собирать материалы по теме, проходить обучающие курсы, обращаться к коллегам, набираясь от них опыта.

        В-третьих, быть открытым. Общение с ребятами из разных команд — в офисе, в столовой или возле лифта — поможет найти карьерные возможности. Вдруг в компании как раз ищут специалиста по интересному тебе направлению.Также важно постоянно искать челленджи, которые помогут прокачать те или иные навыки. Например, я сам приходил к коллегам и спрашивал, что могу сделать, чтобы развить навык публичных выступлений.</p>
    <h3>А чего не стоит делать</h3>
    <p>Перед тем как искать новое место работы, — поищите возможности внутри своей компании.

