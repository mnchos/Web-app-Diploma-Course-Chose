    <h3>Soft и hard skills: в чем разница?</h3>
    <p>Все навыки специалистов можно разделить на две категории:
    <p><strong>Hard skills</strong> — все технические навыки, без которых специалист не смог бы выполнять свою работу. Для IT-специалистов это:
        языки программирования, фреймворки, системы виртуализации, принципы построения сетей и так далее. Для подтверждения таких навыков вас могут попросить решить
        кейс прямо на собеседовании, подтвердить прохождение курсов или рассказать примеры ситуаций с предыдущих мест работы.</p>
    <p><strong>Soft skills</strong> — это личностные качества, которые характеризуют манеру поведения на работе, они связаны с профессией только косвенно.
        Они не такие конкретные как hard skills, поэтому и получили название soft (мягкие). Плюс этих навыков в том, что они не относятся к конкретной позиции
        в компании и узкому кругу задач, а скорее характеризуют весь ваш путь и помогают достичь высоких результатов в карьере.
        Примеры важных soft skills: лидерские качества, адаптивность, стрессоустойчивость, креативность, критичный подход, умение
            работать в команде.</p>
    <h3>Для чего вообще нужны soft skills?</h3>
    <p>В первую очередь, для получения этой самой заветной должности за рубежом с релокацией. Когда я готовлю клиентов к собеседованиям в Британии
        и Европе, очень часто вижу, что IT-специалисты недооценивают именно важность гибких навыков при отборе.</p>
    <div class="trblockquote">На soft skills обязательно будут смотреть во время собеседования — как на основе вашего общения с рекрутером
        или менеджером, так и задавая competency-based questions, вопросы о вашем предыдущем опыте.</div>
    <p>Самый главный из таких навыков — коммуникация, то есть умение слышать и давать обратную связь, доносить свою позицию, находить общие решения.
        Этот навык важно показать всем специалистам в области IT: разработчикам, архитекторам, инженерам, продакт менеджерам, проектным менеджерам.
        Эффективная коммуникация в корпорациях,
        хотя не первый взгляд это и не очевидно, помогает находить самые лучшие решения для рабочих задач.
        Личностные качества также помогают в других рабочих вопросах.</p>
    <h3>1. Успешность в команде</h3>
    <p>Умение понимать и взаимодействовать с другими людьми способствует положительному микроклимату, в котором легко рождаются новые идеи.
        В зарубежных корпорациях обычно понимают, что проще взять человека с хорошими коммуникативными качествами, но без некоторых технических навыков,
        чем отличного профессионала, не умеющего общаться с коллегами.
        Технические навыки можно подтянуть с помощью обучения, а вот перестройка личности требует более глубокой работы.
        Для успешного развития в команде стартапа вам наряду с коммуникацией также пригодятся: инициативность, стрессоустойчивость и способность принимать решения.</p>
    <h3>2. Быстрый рост по карьерной лестнице</h3>
    <p>Программист, который занимает активную позицию не только в своем проекте, но и смежных, который умеет слышать, доносить информацию и принимать решения
        имеет огромные шансы перейти на руководящую роль в проекте.<div></div>
        Менеджер проекта, который активно обучается и прислушивается к мнению коллег, даже при пробелах в технических знаниях будет намного ценнее для компании,
        чем тот, кто сосредоточен лишь на выполнении своих задач. Не все специалисты начального звена могут
        развивать продукт, но прелесть soft skills в том, что именно они помогают привлекать дополнительные мнения для решения неординарных задач.</p>
    <h3>3. Репутация</h3>
    <P>Если вас знают как доброжелательного, приятного профессионала, то обязательно порекомендуют
        на должность, к вам придут за советом, вы будете получать заказы через сарафанное радио.</P>
    <div class="trblockquote">Рынок труда в Великобритании и Западной Европе не зря называется “реферальным”: в большинстве
        компаний предусмотрены бонусы сотруднику за рекомендацию подходящего кандидата. Если вы получите реферал на открытую вакансию,
        вас с вероятностью 99% пригласят хотя бы на первое собеседование (а иногда и его получить очень сложно!). Поэтому человеку со слабо
        развитыми личностными качествами бывает довольно проблематично найти работу — его резюме просто пропускают в общем потоке.
    </div>
    <h3>4. Адаптация к новым условиям</h3>
    <p>При смене города или страны меняется все — от вида из окна до совершенно другой корпоративной культуры. Адаптивность и soft skills в этом случае выходят на первый план,
        а именно: умение понять коллег с другим менталитетом, бодрость духа, самоорганизация, проактивность, нетворкинг.</p>
