    <h2>Ключевые soft skills для IT-специалиста за рубежом</h2>
    Опрос рекрутеров в 2021 году показал, что зарубежные компании хотят видеть в сотрудниках независимость,
    умение работать в команде, гибкость и разные подходы к решению проблем. В этой статье я расскажу,
    какие «гибкие навыки» нужны именно ИТ-специалистам за границей и как правильно такие навыки преподнести.
    <p>В межсезонье сотрудники чаще задумываются о том, чтобы сменить работу. В их числе — IT-специалисты.
        Мы попросили руководителей отделов разработки и HR-менеджеров рассказать, на какие софт-скилы они обращают внимание при найме.</p>
