        работаете в одиночестве. Тогда нужно принять для себя решение: развивать новые личностные качества или поискать более подходящую должность.</p>
    <h3>Как прописывать soft skills в резюме?</h3>
    <P>В самом начале резюме сделайте раздел Summary или Profile, где в 3–5 строчках расскажете об опыте и вашем соответствии позиции, на которую подаетесь.
        Здесь всегда есть соблазн указать что-то общее: аккуратность, организованность, внимание к деталям… но лучше указать несколько релевантных
        для роли навыков (опираясь на пример анализа выше), на которые намекает или упоминает напрямую описание вакансии.</P>
    <div class="trblockquote">При этом важно показать soft skills и через основную часть резюме, где вы рассказываете про компетенции и опыт. Упомяните среди достижений, как вы организовали рабочий процесс с сильной экономией времени для компании. Или как вы презентовали свои решения клиентам или руководству и помогли бизнесу получить новый проект.</div>
    <P>Если не очень получается показать личностные качестве через резюме, пишите в сопроводительном письме. Так вы не просто расскажете о своих навыках, но и покажете их через конкретные примеры, что гораздо убедительнее, чем просто перечисление.</P>
    <h3>Как проверяют soft skills на собеседовании?</h3>
    <div class="trblockquote">Важнейший этап отбора соискателей — это поведенческое интервью или интервью по компетенциям. Его включают в отбор как раз для того, чтобы проверить ваши личностные качества. Вопросы на этом этапе задаются предельно конкретные, чаще всего они начинаются с “Как вы поступали в ситуации, когда..?” или “Что бы вы сделали, если..? </div>
    <p>Примеры вопросов на интервью по компетенциям:</p>
    <ul>
        <li>Сталкивались ли вы с нестандартными задачами? Расскажите о такой ситуации.</li>
        <li>Приходилось ли вам решать сложные задачи на последней работе? Приведите пример.</li>
        <li>Расскажите, как вы действовали, когда у вас был конфликт с другим сотрудником.</li>
    </ul>
    <p>В идеале, в ответ на эти вопросы нужно приводить емко описанные примеры по<strong> структуре STAR (Situation, Task, Action, Result):</strong></p>
    <ul>
        <li>1.О какой ситуации вы сейчас говорите? Что это была за компания, департамент, ваша роль?</li>
        <li>2.Какая задача стояла перед вами и в чем была проблема?</li>
        <li>3.Как вы подошли к решению этой задачи, что вы сделали?</li>
        <li>4.Какой результат был в итоге? Например, проблема безболезненно решилась с минимальными потерями для
            компании или улучшились отношения с другим отделом или работодатель получил высокую прибыль.</li>
    </ul>
    <div class="trblockquote">Заготовьте 10–12 коротких историй для описания разных навыков, используя подходящую лексику.
        Иначе при таком вопросе легко растеряться и дать очень поверхностный и неконкретный ответ.
    </div>
    <p>В моей практике был случай проведения поведенческого интервью, когда соискатель на каждый вопрос отвечал около 10 минут. Когда мы ему намекнули, что нужно чуть больше сконцентрироваться на сути, он сразу стал отвечать короче и по делу.
        Тем самым он показал такие качества как умение слушать и адаптивность, и в конечном итоге, получил оффер.</p>
    <h3>Оценочные центры и soft skills</h3>
    <p>Assessment centres — это обычная практика для крупных корпораций на финальной стадии отбора соискателей, когда их осталось уже не так много. Кандидатов приглашают физически прийти в офис или виртуально, решить несколько тестов и разного рода задач. Также обычно есть групповое задание, где всех оценивают по определенным шкалам soft skills, которые очень важны компаниям.
    </p>
    <div class="trblockquote">Расскажу пример группового упражнения из моей практики проведения центра оценки. В офис приходит 5–7 кандидатов, которым сообщаются условия задачи: «У компании есть средства в размере ХХ и они хотят вложить их в интересный проект». Каждый участник получает брифинг на свой проект, знакомится с деталями и презентует их остальным. Через 30 минут кандидаты должны решить, в какой проект вкладывать деньги. Всё это время в комнате находится несколько рекрутеров, которые наблюдают за поведением кандидатов.

    </div>
    <p>В рамках этого задания оцениваются:

    </p>
    <ul>
        <li>Коммуникативные навыки кандидатов (как общаются внутри группы, как презентуют проект).
        </li>
        <li>Навыки командной работы (например, сразу видны кандидаты, которые упорно продвигают свою точку зрения и не хотят идти на компромисс).
        </li>
        <li>Адаптивность и открытость к чужому мнению. Важно понимать, что когда принятые соискатели будут работать в команде, они скорее всего будут работать по методологии Agile, где меняются как требования, так и приоритеты. Поэтому нужно показать свою способность изменить мнение и подстроиться под группу, если это необходимо.
        </li>
    </ul>
    <h3>Как улучшить soft skills?
    </h3>
    <p>Все начинается с желания. Подумайте, какие навыки вам хотелось бы улучшить и пропишите самые комфортные способы для вас. Примеры:

    </p>
    <ul>
        <li>1.Пробуйте менять впечатление недовольных клиентов через хорошие, дружелюбные отношения. Вы прокачаете креативность,
            стрессоустойчивость, коммуникативные навыки.
        </li>
        <li>2.Научитесь решать какую-то типичную проблему на работе и поделитесь своей экспертизой с коллегами. Вы прокачаете
            навыки кризис-менеджмента, коучинговые навыки, коммуникацию, критическое мышление.
        </li>
        <li>3.Стройте гипотезы по развитию продукта/улучшению функциональности и оговаривайте это с командой. Вы прокачаете проактивность, креативность, коммуникацию.
        </li>
        <li>4.Участвуйте в конференциях в качестве спикера. Вы прокачаете уверенность в себе, коммуникацию, организованность, структурирование информации.
        </li>
        <li>5.Участвуйте в хакатонах. Вы сможете прокачать лидерские навыки, тайм-менеджмент, творческое мышление.
        </li>
        <li>6.Поставьте себе цели в самообразовании. Поймите, в чем вам хотелось бы развиваться, и запишитесь на курсы,
            тренинг, начните изучать (улучшать) иностранный язык. Помимо hard skills вы сможете прокачать креативность, уверенность, целеустремленность.
        </li>

    </ul>
    <p>В целом, компании ищут не самого умного и подкованного во всем кандидата, а наиболее подходящего под конкретную
        роль c определенными задачами. Личностные навыки при этом играют огромную роль. Внимательно читайте описания вакансий,
        адаптируйте свое резюме под каждую многообещающую должность, изучайте ценности и корпоративную культуру компаний — через публикации
        в их соцсетях и блоге, официальный сайт или через знакомых, работающих там.

